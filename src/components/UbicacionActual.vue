// UbicacionActual.vue

<template>
  <div>
    <button @click="obtenerUbicacion">Obtener Ubicaci칩n</button>
    <div v-if="ubicacion">
      <p>Latitud: {{ ubicacion.lat }}</p>
      <p>Longitud: {{ ubicacion.lon }}</p>
    </div>
  </div>
</template>

<script>
export default {
  data() {
    return {
      ubicacion: null
    };
  },
  methods: {
    obtenerUbicacion() {
      if (navigator.geolocation) {
        navigator.geolocation.getCurrentPosition(
          this.obtenerCoordenadas,
          this.errorObtencionUbicacion
        );
      } else {
        console.log("Geolocalizaci칩n no est치 disponible en este navegador.");
      }
    },
    obtenerCoordenadas(posicion) {
      this.ubicacion = {
        lat: posicion.coords.latitude,
        lon: posicion.coords.longitude
      };
    },
    errorObtencionUbicacion(error) {
      console.error("Error al obtener la ubicaci칩n: ", error.message);
    }
  }
};
</script>
